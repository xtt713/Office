        #region ConStrintSwitch
        public string ConStrintSwitch(string connStr)
        {
            if (connStr != "")
            {
                if (connStr.Contains("\\"))
                {
                    string tempStr = connStr.Substring(connStr.IndexOf("\\") + 1);
                    connStr = connStr.Substring(0, connStr.IndexOf("\\") + 1);
                    connStr += "\\";
                    connStr += tempStr;
                }
            }

            return connStr;
        }

